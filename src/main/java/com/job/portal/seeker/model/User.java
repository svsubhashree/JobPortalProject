package com.job.portal.seeker.model;

/**
 * User pojo class refers to users entity
 * 
 * @author subhashree
 */
public class User {

	private String email;
	private String firstname;
	private String lastname;
	private String desiredusername;
	private String password;
	private String gender;
	private String totalexp;
	private String jobprefer;
	private String keyskills;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getDesiredusername() {
		return desiredusername;
	}
	public void setDesiredusername(String desiredusername) {
		this.desiredusername = desiredusername;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getTotalexp() {
		return totalexp;
	}
	public void setTotalexp(String totalexp) {
		this.totalexp = totalexp;
	}
	public String getJobprefer() {
		return jobprefer;
	}
	public void setJobprefer(String jobprefer) {
		this.jobprefer = jobprefer;
	}
	public String getKeyskills() {
		return keyskills;
	}
	public void setKeyskills(String keyskills) {
		this.keyskills = keyskills;
	}
	
	
}
